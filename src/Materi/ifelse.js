let strukturIfElseSimple = (x, y) => {
if(x>y)console.log("Nilai X Lebih Besar Dari Y")
else console.log("Nilai Y Lebih Besar Dari X")
}

strukturIfElseSimple();
// secara default memberikan nilai false
strukturIfElseSimple(23, 22);

